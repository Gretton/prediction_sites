-- match_statistics table for Namecheap
-- Run this in phpMyAdmin SQL tab on the prediksa_web database

CREATE TABLE IF NOT EXISTS `match_statistics` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `api_fixture_id` int(11) NOT NULL,
  `match_date` date NOT NULL,
  `league_name` varchar(255) DEFAULT NULL,
  `league_id_api` int(11) DEFAULT NULL,
  `home_team_api` varchar(255) DEFAULT NULL,
  `away_team_api` varchar(255) DEFAULT NULL,
  `home_score` int(11) DEFAULT NULL,
  `away_score` int(11) DEFAULT NULL,
  `referee` varchar(255) DEFAULT NULL,
  `venue` varchar(255) DEFAULT NULL,
  `home_shots_on_goal` int(11) DEFAULT NULL,
  `away_shots_on_goal` int(11) DEFAULT NULL,
  `home_shots_off_goal` int(11) DEFAULT NULL,
  `away_shots_off_goal` int(11) DEFAULT NULL,
  `home_total_shots` int(11) DEFAULT NULL,
  `away_total_shots` int(11) DEFAULT NULL,
  `home_blocked_shots` int(11) DEFAULT NULL,
  `away_blocked_shots` int(11) DEFAULT NULL,
  `home_shots_inside_box` int(11) DEFAULT NULL,
  `away_shots_inside_box` int(11) DEFAULT NULL,
  `home_shots_outside_box` int(11) DEFAULT NULL,
  `away_shots_outside_box` int(11) DEFAULT NULL,
  `home_ball_possession` varchar(10) DEFAULT NULL,
  `away_ball_possession` varchar(10) DEFAULT NULL,
  `home_corner_kicks` int(11) DEFAULT NULL,
  `away_corner_kicks` int(11) DEFAULT NULL,
  `home_offsides` int(11) DEFAULT NULL,
  `away_offsides` int(11) DEFAULT NULL,
  `home_free_kicks` int(11) DEFAULT NULL,
  `away_free_kicks` int(11) DEFAULT NULL,
  `home_fouls` int(11) DEFAULT NULL,
  `away_fouls` int(11) DEFAULT NULL,
  `home_yellow_cards` int(11) DEFAULT NULL,
  `away_yellow_cards` int(11) DEFAULT NULL,
  `home_red_cards` int(11) DEFAULT NULL,
  `away_red_cards` int(11) DEFAULT NULL,
  `home_goalkeeper_saves` int(11) DEFAULT NULL,
  `away_goalkeeper_saves` int(11) DEFAULT NULL,
  `home_total_passes` int(11) DEFAULT NULL,
  `away_total_passes` int(11) DEFAULT NULL,
  `home_passes_accurate` int(11) DEFAULT NULL,
  `away_passes_accurate` int(11) DEFAULT NULL,
  `home_pass_accuracy` varchar(10) DEFAULT NULL,
  `away_pass_accuracy` varchar(10) DEFAULT NULL,
  `home_expected_goals` decimal(5,2) DEFAULT NULL,
  `away_expected_goals` decimal(5,2) DEFAULT NULL,
  `home_goals_prevented` decimal(5,2) DEFAULT NULL,
  `away_goals_prevented` decimal(5,2) DEFAULT NULL,
  `raw_statistics` longtext DEFAULT NULL,
  `raw_fixture` longtext DEFAULT NULL,
  `collected_at` timestamp NOT NULL DEFAULT current_timestamp(),
  PRIMARY KEY (`id`),
  UNIQUE KEY `uq_api_fixture` (`api_fixture_id`),
  KEY `idx_match_date` (`match_date`),
  KEY `idx_league` (`league_name`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
